// Sure, here's a brief explanation of the differences between the for loop, while loop, and do-while loop:

// For Loop:

// Syntax: for (initialization; condition; increment/decrement)
// A for loop is used when you know exactly how many times you want to iterate.
// It consists of three parts:
// Initialization: Executes once before the loop starts.
// Condition: Checked before each iteration. If true, the loop continues; otherwise, it exits.
// Increment/Decrement: Executed at the end of each iteration.
// Example:
// java
// Copy code
// for (int i = 0; i < 5; i++) {
//     System.out.println(i);
// }
// While Loop:

// Syntax: while (condition)
// A while loop is used when you don't know in advance how many times the loop will run, but you know the condition under which it should continue.
// It checks the condition before each iteration.
// Example:
// java
// Copy code
// int i = 0;
// while (i < 5) {
//     System.out.println(i);
//     i++;
// }
// Do-While Loop:

// Syntax: do { /* code */ } while (condition)
// A do-while loop is similar to a while loop, but it guarantees that the loop body will be executed at least once before the condition is checked.
// It checks the condition after each iteration.
// Example:
// java
// Copy code

// int i = 0;
// do {
//     System.out.println(i);
//     i++;
// } while (i < 5);
// In summary, for loops are used when you know the number of iterations in advance,
// while loops are used when you're unsure about the number of iterations but have a condition to check,
// do-while loops are used when you want the loop body to execute at least once, regardless of the condition.









